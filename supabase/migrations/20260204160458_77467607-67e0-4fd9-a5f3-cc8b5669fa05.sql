-- Fix search_path on trigger functions for security
CREATE OR REPLACE FUNCTION public.update_referrals_updated_at()
RETURNS TRIGGER
LANGUAGE plpgsql
SECURITY INVOKER
SET search_path = public
AS $$
BEGIN
    NEW.updated_at = now();
    RETURN NEW;
END;
$$;

